/*
Problem:数组长1001,值范围是1-1000,含有1-1000,还有一个出现了两次的值,找出它
Solution:由于范围固定,且1-1000都出现了一次,要找的数还会出现1次,故我们给这1001个数取异或之后,
再对1-1000取异或,相当于重复的数字出现三次,其余出现了两次
*/
	
	int A[1001];
    for(int i=0; i<1000; ++i)
        A[i]=i+1;
    A[1000]=888;
    int res=A[0];
    for(int i=1; i<1001; ++i)
        res^=(A[i]^i);  //同时再对1-1000取异或
    cout<<res<<endl;